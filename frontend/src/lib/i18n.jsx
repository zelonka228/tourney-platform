const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem("arena_lang") || "ua");
  const change = useCallback((l) => {
    setLang(l);
    localStorage.setItem("arena_lang", l);
  }, []);
  const t = useCallback(
    (key, vars) => {
      let s = DICT[lang]?.[key] ?? DICT.ua[key] ?? key;
      if (vars) for (const k in vars) s = s.replace(`{${k}}`, vars[k]);
      return s;
    },
    [lang]
  );
  return (
    <I18nContext.Provider value={{ lang, setLang: change, t }}>{children}</I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
